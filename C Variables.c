#include <stdio.h>

int main() {
    int age;        // Variable declaration
    age = 25;       // Variable initialization

    float salary = 5000.0;  // Variable declaration and initialization

    char letter = 'A';  // Variable declaration and initialization

    printf("Age: %d\n", age);  // Printing the value of age
    printf("Salary: %.2f\n", salary);  // Printing the value of salary
    printf("Letter: %c\n", letter);  // Printing the value of letter

    return 0;
}